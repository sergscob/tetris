export const createPiece = (type, overrides = {}) => ({
  type,
  rotation: 0,
  x: type==='O' ? 4 : 3,
  y: -1,
  ...overrides,
})


export const getPieceCells = piece => 
  SHAPES[piece.type][piece.rotation].map(([row, col]) => ({ x: piece.x + col, y: piece.y + row }))


export const rotatePiece = piece => ({
  ...piece,
  rotation: (piece.rotation + 1) % 4,
})
